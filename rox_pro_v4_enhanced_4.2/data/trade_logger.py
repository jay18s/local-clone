    # Bootstrap from existing trade history
    # ------------------------------------------------------------------

    def _bootstrap_from_history(self):
        """Load closed trade outcomes into the accuracy store on startup."""
        try:
            trades = self.trade_logger.data_manager.get_trade_history(limit=500)
            closed = [t for t in trades if t.get("date_closed") and t.get("pnl_pct") is not None]
            for trade in closed:
                self.record_outcome(trade)
            if closed:
                self.logger.info(f"Bootstrapped {len(closed)} historical outcomes into learning module")
        except Exception as e:
            self.logger.warning(f"Could not bootstrap learning module: {e}")
