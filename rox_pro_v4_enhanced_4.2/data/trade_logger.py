class ContinuousLearningModule:
    """
    ROX Continuous Learning Module
    ================================
    Closes the feedback loop between predictions and market outcomes.

    Capabilities
    ------------
    1. Prediction Accuracy Tracking  – per-agent win/loss/accuracy over time
    2. Winning/Losing Pattern Analysis – identifies which setups work best
    3. Regime-Adaptive Weight Adjustment – boosts agents that excel in current regime
    4. Seasonality Analysis – Budget month, result seasons, F&O expiry effects
    """

    # Regime tags for classification
    REGIME_LABELS = ["BULL", "BEAR", "CONSOLIDATION", "HIGH_VOLATILITY"]

    # Seasonal calendar (month -> context)
    SEASONAL_CALENDAR = {
        1:  {"label": "Budget Month",        "bias": "VOLATILE",   "note": "Pre-budget rally / sell-off"},
        2:  {"label": "Budget Aftermath",    "bias": "TRENDING",   "note": "Post-budget trend establishment"},
        3:  {"label": "Q3 Results",          "bias": "STOCK_PICK", "note": "Q3 earnings season"},
        4:  {"label": "FY End / New FY",     "bias": "VOLATILE",   "note": "Mutual fund rebalancing, advance tax"},
        5:  {"label": "Q4 Results",          "bias": "STOCK_PICK", "note": "Full-year results, dividend announcements"},
        6:  {"label": "Pre-Monsoon",         "bias": "NEUTRAL",    "note": "Monsoon forecast impact on FMCG/Agro"},
        7:  {"label": "Q1 Results",          "bias": "STOCK_PICK", "note": "Q1 earnings season"},
        8:  {"label": "Monsoon Mid",         "bias": "NEUTRAL",    "note": "Sector rotation: Pharma, Cement"},
        9:  {"label": "FII Rebalancing",     "bias": "VOLATILE",   "note": "Global fund rebalancing, DII flows"},
        10: {"label": "Q2 Results",          "bias": "STOCK_PICK", "note": "Q2 earnings + Diwali rally"},
        11: {"label": "Diwali / FII Flows",  "bias": "BULLISH",    "note": "Historically strong month"},
        12: {"label": "Year-End Drift",      "bias": "LOW_VOL",    "note": "Holiday thin-volumes, tax harvesting"},
    }

    # F&O expiry effect window (days before expiry where volatility picks up)
    FNO_EXPIRY_ALERT_DAYS = 5

    def __init__(self, trade_logger: 'TradeLogger'):
        self.trade_logger = trade_logger
        self.logger = __import__('logging').getLogger("ContinuousLearning")

        # In-memory accuracy store: {agent: {regime: {wins, total}}}
        self._accuracy_store: dict = _defaultdict(
            lambda: _defaultdict(lambda: {"wins": 0, "total": 0, "pnl_sum": 0.0})
        )

        # Pattern success tracker: {pattern_name: {wins, total}}
        self._pattern_store: dict = _defaultdict(lambda: {"wins": 0, "total": 0, "avg_pnl": 0.0})

        # Weight history for explainability
        self._weight_history: list = []

        # Load from trade history on init
        self._bootstrap_from_history()

    # ------------------------------------------------------------------
    # 1. Prediction Accuracy Feedback Loop
    # ------------------------------------------------------------------

    def record_outcome(self, trade_record: dict):
        """
        Record a closed trade outcome into the learning store.

        trade_record keys:
            stock, direction, recommending_agents (pipe-separated), regime_at_entry,
            conviction_confidence, pnl_pct, exit_reason, pattern (optional)
        """
        pnl = float(trade_record.get("pnl_pct", 0.0))
        won = pnl > 0
        regime = trade_record.get("regime_at_entry", "UNKNOWN").upper()
        agents = [a.strip() for a in trade_record.get("recommending_agents", "").split("|") if a.strip()]
        pattern = trade_record.get("pattern", "")

        for agent in agents:
            store = self._accuracy_store[agent][regime]
            store["total"] += 1
            store["pnl_sum"] += pnl
            if won:
                store["wins"] += 1

        if pattern:
            ps = self._pattern_store[pattern]
            ps["total"] += 1
            ps["avg_pnl"] = (ps["avg_pnl"] * (ps["total"] - 1) + pnl) / ps["total"]
            if won:
                ps["wins"] += 1

        self.logger.debug(f"Outcome recorded: {trade_record.get('stock')} | "
                          f"P&L: {pnl:.2f}% | Won: {won}")

    def get_agent_accuracy(self, agent: str, regime: str = None) -> dict:
        """
        Returns accuracy stats for an agent, optionally filtered by regime.
        """
        if regime:
            data = self._accuracy_store[agent].get(regime.upper(), {"wins": 0, "total": 0, "pnl_sum": 0.0})
            total = data["total"]
            wins = data["wins"]
            return {
                "agent": agent,
                "regime": regime,
                "wins": wins,
                "total": total,
                "accuracy_pct": round(wins / total * 100, 1) if total else 0,
                "avg_pnl_pct": round(data["pnl_sum"] / total, 2) if total else 0,
            }

        # Aggregate across all regimes
        all_wins = sum(d["wins"] for d in self._accuracy_store[agent].values())
        all_total = sum(d["total"] for d in self._accuracy_store[agent].values())
        all_pnl = sum(d["pnl_sum"] for d in self._accuracy_store[agent].values())
        return {
            "agent": agent,
            "regime": "ALL",
            "wins": all_wins,
            "total": all_total,
            "accuracy_pct": round(all_wins / all_total * 100, 1) if all_total else 0,
            "avg_pnl_pct": round(all_pnl / all_total, 2) if all_total else 0,
        }

    def print_accuracy_matrix(self) -> str:
        """Formatted accuracy matrix across agents and regimes."""
        agents = sorted(self._accuracy_store.keys())
        regimes = self.REGIME_LABELS
        lines = ["PREDICTION ACCURACY MATRIX", "-" * 70]
        header = f"{'Agent':<12}" + "".join(f"{r:>18}" for r in regimes)
        lines.append(header)
        lines.append("-" * 70)
        for agent in agents:
            row = f"{agent:<12}"
            for regime in regimes:
                stats = self.get_agent_accuracy(agent, regime)
                if stats["total"] > 0:
                    row += f"  {stats['accuracy_pct']:>5.1f}% ({stats['total']:>3})"
                else:
                    row += f"{'—':>18}"
            lines.append(row)
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # 2. Winning / Losing Pattern Analysis
    # ------------------------------------------------------------------

    def analyze_patterns(self, min_sample: int = 5) -> list:
        """
        Returns a ranked list of patterns by win-rate (minimum sample enforced).
        Each entry: {pattern, win_rate, avg_pnl, total_trades}
        """
        results = []
        for pattern, stats in self._pattern_store.items():
            if stats["total"] < min_sample:
                continue
            win_rate = stats["wins"] / stats["total"]
            results.append({
                "pattern": pattern,
                "win_rate_pct": round(win_rate * 100, 1),
                "avg_pnl_pct": round(stats["avg_pnl"], 2),
                "total_trades": stats["total"],
            })
        results.sort(key=lambda x: x["win_rate_pct"], reverse=True)
        return results

    def get_best_patterns(self, top_n: int = 5) -> list:
        return self.analyze_patterns()[:top_n]

    def get_worst_patterns(self, top_n: int = 5) -> list:
        return self.analyze_patterns()[-top_n:]

    def pattern_analysis_report(self) -> str:
        patterns = self.analyze_patterns()
        lines = ["PATTERN PERFORMANCE ANALYSIS", "-" * 55]
        lines.append(f"{'Pattern':<30} {'Win%':>6} {'AvgPnL%':>8} {'Trades':>6}")
        lines.append("-" * 55)
        for p in patterns:
            lines.append(
                f"{p['pattern']:<30} {p['win_rate_pct']:>5.1f}% "
                f"{p['avg_pnl_pct']:>+7.2f}% {p['total_trades']:>6}"
            )
        if not patterns:
            lines.append("  Insufficient data (min 5 trades per pattern required)")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # 3. Regime-Adaptive Weight Adjustment
    # ------------------------------------------------------------------

    def compute_weight_adjustments(self, current_regime: str,
                                   base_weights: dict,
                                   min_weight: float = 0.05,
                                   max_weight: float = 0.35,
                                   learning_rate: float = 0.03) -> dict:
        """
        Compute updated agent weights based on historical accuracy in the
        current market regime. High-accuracy agents get a boost; poor-
        accuracy agents get trimmed. Uses bounded gradient nudge.

        Parameters
        ----------
        current_regime  : str   – e.g. "BULL"
        base_weights    : dict  – {agent_name: current_weight (0–1)}
        learning_rate   : float – how aggressively to adjust (default 3%)

        Returns
        -------
        dict – {agent_name: new_weight}
        """
        regime = current_regime.upper()
        adjustments = {}

        total_weight = sum(base_weights.values()) or 1.0

        for agent, weight in base_weights.items():
            stats = self.get_agent_accuracy(agent, regime)
            total_trades = stats["total"]

            if total_trades < 5:
                # Not enough data – keep existing weight
                adjustments[agent] = weight
                continue

            accuracy = stats["accuracy_pct"] / 100.0

            # Delta: positive for high accuracy, negative for low
            delta = (accuracy - 0.5) * learning_rate * 2
            new_weight = max(min_weight, min(max_weight, weight + delta))
            adjustments[agent] = round(new_weight, 4)

        # Normalize so weights sum to 1.0
        total = sum(adjustments.values()) or 1.0
        normalized = {k: round(v / total, 4) for k, v in adjustments.items()}

        # Log adjustment event
        self._weight_history.append({
            "timestamp": __import__('datetime').datetime.now().isoformat(),
            "regime": regime,
            "weights": normalized,
        })

        self.logger.info(f"Weight adjustments computed for regime={regime}: {normalized}")
        return normalized

    def weight_adjustment_report(self, current_regime: str, base_weights: dict) -> str:
        new_weights = self.compute_weight_adjustments(current_regime, base_weights)
        lines = [f"WEIGHT ADJUSTMENT REPORT — Regime: {current_regime}", "-" * 50]
        lines.append(f"{'Agent':<12} {'Old Weight':>12} {'New Weight':>12} {'Delta':>10}")
        lines.append("-" * 50)
        for agent, new_w in sorted(new_weights.items()):
            old_w = base_weights.get(agent, 0.0)
            delta = new_w - old_w
            sign = "▲" if delta > 0 else ("▼" if delta < 0 else "─")
            lines.append(f"{agent:<12} {old_w:>11.3f}  {new_w:>11.3f}  {sign}{abs(delta):.3f}")
        return "\n".join(lines)

    # ------------------------------------------------------------------
    # 4. Seasonality Analysis
    # ------------------------------------------------------------------

    def get_seasonality_context(self, dt: 'date' = None) -> dict:
        """
        Return the current seasonal trading context:
        month label, market bias, and F&O expiry proximity.

        dt: date object (defaults to today)
        """
        from datetime import date, timedelta
        if dt is None:
            dt = date.today()

        month_info = self.SEASONAL_CALENDAR.get(dt.month, {
            "label": "Unknown", "bias": "NEUTRAL", "note": ""
        })

        # F&O expiry: last Thursday of each month
        fno_expiry = self._last_thursday_of_month(dt.year, dt.month)
        days_to_expiry = (fno_expiry - dt).days
        near_expiry = 0 <= days_to_expiry <= self.FNO_EXPIRY_ALERT_DAYS

        return {
            "date": dt.isoformat(),
            "month": dt.month,
            "month_label": month_info["label"],
            "seasonal_bias": month_info["bias"],
            "seasonal_note": month_info["note"],
            "fno_expiry_date": fno_expiry.isoformat(),
            "days_to_fno_expiry": days_to_expiry,
            "near_fno_expiry": near_expiry,
            "expiry_alert": (
                f"⚠️ F&O expiry in {days_to_expiry} day(s) — expect elevated IV/premium decay"
                if near_expiry else ""
            ),
        }

    @staticmethod
    def _last_thursday_of_month(year: int, month: int) -> 'date':
        """Return the last Thursday (weekday=3) of a given month."""
        from datetime import date
        import calendar
        last_day = calendar.monthrange(year, month)[1]
        for day in range(last_day, last_day - 7, -1):
            if date(year, month, day).weekday() == 3:  # Thursday
                return date(year, month, day)
        return date(year, month, last_day)

    def seasonality_report(self) -> str:
        ctx = self.get_seasonality_context()
        lines = [
            "SEASONALITY ANALYSIS",
            "-" * 45,
            f"  Date           : {ctx['date']}",
            f"  Month Theme    : {ctx['month_label']}",
            f"  Seasonal Bias  : {ctx['seasonal_bias']}",
            f"  Note           : {ctx['seasonal_note']}",
            f"  F&O Expiry     : {ctx['fno_expiry_date']} (T-{ctx['days_to_fno_expiry']}d)",
        ]
        if ctx["expiry_alert"]:
            lines.append(f"  {ctx['expiry_alert']}")
        return "\n".join(lines)

